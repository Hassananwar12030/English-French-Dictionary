    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // System.out.println("Inserted Words: "+X.FR.countInserted);
         
         if(FrenchWord.getText().isEmpty()){
                      EnglishMeaning.setText("<html>"+"Please enter a word."+"</html>");

         }
         else{
             
             String FW=FrenchWord.getText().trim().toLowerCase();
             
             Node M=X.FR.searchFW(FW);
             
             String EM=M.EnglishMeaning;
             String T=M.Type;
             
            
             
             
             
         EnglishMeaning.setText("<html>"+EM+"</html>");
         
         Type.setText(T);

         }

    }//GEN-LAST:event_jButton1ActionPerformed

